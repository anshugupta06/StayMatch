let choreChart = null;

async function generateChores() {

    const output = document.getElementById("output");
    const btn = document.querySelector(".glow-btn");

    const data = {
        name1: document.getElementById("name1").value.trim(),
        name2: document.getElementById("name2").value.trim(),
        chores: document.getElementById("chores").value.trim(),
        clean1: +document.getElementById("clean1").value,
        clean2: +document.getElementById("clean2").value
    };

    if (!data.name1 || !data.name2 || !data.chores) {
        alert("Fill all fields ⚠️");
        return;
    }

    btn.innerText = "Neural Balancing...";
    btn.disabled = true;

    try {

        /* 🧠 DIFFICULTY MAP */
        const difficultyMap = {
            dishes: 1,
            trash: 1,
            vacuum: 2,
            cooking: 2,
            laundry: 2,
            bathroom: 3,
            mopping: 3
        };

        const chores = data.chores.split(",").map(c => c.trim());

        let scoreA = 0, scoreB = 0;

        let plan = `===== Smart Chore Plan =====\n\n`;
        plan += `👥 ${data.name1} & ${data.name2}\n\n`;

        const days = ["Monday","Tuesday","Wednesday","Thursday","Friday","Saturday","Sunday"];

        plan += `--- Weekly Schedule ---\n`;

        chores.forEach((chore, i) => {

            const key = chore.toLowerCase();
            const weight = difficultyMap[key] || 2;

            let assigned;

            /* 🤖 CLEANLINESS-AWARE AI */
            const prefA = data.clean1 / 10;
            const prefB = data.clean2 / 10;

            if ((scoreA * prefA) <= (scoreB * prefB)) {
                assigned = data.name1;
                scoreA += weight;
            } else {
                assigned = data.name2;
                scoreB += weight;
            }

            const day = days[i % 7];
            const heavyTag = weight >= 3 ? " 🔥" : "";

            plan += `• ${day}: ${chore}${heavyTag} (${weight}) → ${assigned}\n`;
        });

        /* 🔁 ROTATION */
        plan += `\n--- Next Week Rotation ---\n`;

        chores.forEach((chore, i) => {
            const day = days[i % 7];
            const assigned = (i % 2 === 0) ? data.name2 : data.name1;
            plan += `• ${day}: ${chore} → ${assigned}\n`;
        });

        /* 🤝 COMPATIBILITY */
        const compatibility =
            100 - (Math.abs(data.clean1 - data.clean2) * 6);

        if (compatibility < 50) {
            plan += `\n⚠️ Low compatibility → assign independent zones.\n`;
        } else if (compatibility < 75) {
            plan += `\n⚠️ Moderate compatibility → structured rotation advised.\n`;
        } else {
            plan += `\n✨ High compatibility → flexible sharing.\n`;
        }

        /* 📊 STATS */
        const total = scoreA + scoreB || 1;
        const ratio = (scoreA / total) * 100;

        plan += `\n📊 Summary:\n`;
        plan += `${data.name1}: ${scoreA} points\n`;
        plan += `${data.name2}: ${scoreB} points\n`;

        const fairness = 100 - Math.abs(scoreA - scoreB) * 5;
        plan += `\n⚖️ Fairness Score: ${Math.max(0, fairness)}%\n`;

        plan += `\n🚀 Generated by StayMatch AI`;

        /* 🖋️ TYPEWRITER */
        typeWriter(plan, () => {

            document.getElementById("dashboard").style.display = "block";
            document.getElementById("chartDashboard").style.display = "block";

            const fill = document.getElementById("balanceFill");
            fill.style.width = ratio + "%";

            fill.style.background =
                (ratio > 40 && ratio < 60) ? "#27c93f" : "#ffbd2e";

            document.getElementById("balanceText").innerText =
                `Distribution: ${data.name1} (${Math.round(ratio)}%) vs ${data.name2} (${Math.round(100 - ratio)}%)`;

            renderChart(data.name1, data.name2, scoreA, scoreB);

        });

    } catch (err) {
        console.error(err);
        output.innerHTML = `<span style="color:red">⚠️ ERROR</span>`;
    } finally {
        btn.innerText = "Generate Smart Schedule";
        btn.disabled = false;
    }
}

/* 📊 PIE CHART */
function renderChart(name1, name2, scoreA, scoreB) {

    const ctx = document.getElementById("choreChart");

    if (choreChart) choreChart.destroy();

    choreChart = new Chart(ctx, {
        type: 'pie',
        data: {
            labels: [name1, name2],
            datasets: [{
                data: [scoreA, scoreB],
                backgroundColor: ['#5c54e5', '#00f2fe']
            }]
        }
    });
}

/* 🖋️ TYPEWRITER */
function typeWriter(text, callback) {

    const output = document.getElementById("output");
    output.innerHTML = "";
    let i = 0;

    function typing() {
        if (i < text.length) {

            let current = text.substring(0, i + 1);

            output.innerHTML = current
                .replace(/(Monday|Tuesday|Wednesday|Thursday|Friday|Saturday|Sunday)/g,
                    '<span style="color:#00f2fe">$1</span>')
                .replace(/(Dishes|Trash|Vacuum|Cooking|Bathroom)/gi,
                    '<span style="color:#9b5cff">$1</span>');

            i++;
            setTimeout(typing, 5);
        } else if (callback) callback();
    }
    typing();
}

/* 📥 DOWNLOAD */
function downloadChorePlan() {

    const text = document.getElementById("output").innerText;

    if (!text.length) {
        alert("No plan to download!");
        return;
    }

    const blob = new Blob([text], { type: "text/plain" });
    const link = document.createElement("a");

    link.href = URL.createObjectURL(blob);
    link.download = `StayMatch_Chores_${Date.now()}.txt`;

    link.click();
}
